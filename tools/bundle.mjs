#!/usr/bin/env node
/* bundle.mjs — Trivial single-file bundler for the runtime.
 *
 * Walks the runtime's import graph starting from poc/runtime.js, resolves
 * every relative `import { x } from "./y"` statement, inlines them into a
 * single ESM file (dist/runtime.mjs) plus a CJS shim (dist/runtime.cjs).
 *
 * No transpilation, no minification — just concatenation with import-graph
 * ordering. Suitable for the 12-module ACS runtime which is already
 * modern ES2022.
 *
 * Why no esbuild/rollup? Zero dependencies — `npm install` on this
 * package adds nothing to the user's lockfile.
 */

import { readFileSync, writeFileSync, mkdirSync, existsSync, copyFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");
const ENTRY = resolve(ROOT, "poc/runtime.js");
const OUT_DIR = resolve(ROOT, "dist");

const visited = new Map(); // path → { code, exports: Set, importsResolved }
const order = [];

// Track `import * as ns from "./foo.js"` cases per module — bundler needs
// to synthesize a `const ns = { ...exports of foo.js };` shim because
// stripping the import line otherwise leaves `ns` undefined at use sites.
const namespaceImports = new Map(); // importing-file → [{ ns, depPath }]
const moduleExports = new Map();    // module path → string[] of exported names
// `import { x as y } from "..."` — y wouldn't exist after strip; emit `const y = x;`.
const renameAliasesPerFile = new Map(); // importing-file → [{ from, to }]

function collectExports(src) {
  const names = new Set();
  // export function/const/let/var/class NAME
  for (const m of src.matchAll(/^export\s+(?:function|const|let|var|class)\s+([a-zA-Z_$][\w$]*)/gm)) {
    names.add(m[1]);
  }
  // export { a, b as c, ... }
  for (const m of src.matchAll(/^export\s*\{([^}]+)\}/gm)) {
    for (const part of m[1].split(",")) {
      const piece = part.trim();
      if (!piece) continue;
      const asMatch = piece.match(/\s+as\s+([a-zA-Z_$][\w$]*)\s*$/);
      names.add(asMatch ? asMatch[1] : piece);
    }
  }
  return [...names];
}

function readAndProcess(path) {
  if (visited.has(path)) return;
  const src = readFileSync(path, "utf8");
  const dir = dirname(path);
  // Match all import forms: `import x from "y"`, `import { a } from "y"`,
  // `import * as ns from "y"`, and side-effect `import "y"`.
  const importRe = /import\s+(?:([\s\S]+?)\s+from\s+)?["']([^"']+)["'];?/g;
  let m;
  const deps = [];
  const nsImports = [];
  const renameAliases = [];
  while ((m = importRe.exec(src))) {
    const spec = m[2];
    const clause = m[1];
    if (!spec.startsWith(".")) continue;
    const depPath = resolve(dir, spec);
    deps.push(depPath);
    if (clause) {
      const nsMatch = clause.match(/\*\s+as\s+([a-zA-Z_$][\w$]*)/);
      if (nsMatch) nsImports.push({ ns: nsMatch[1], depPath });
      // Renamed imports: `{ a as b, c, d as e }`. After import-stripping
      // the symbols `b` and `e` would not exist; emit aliases so they do.
      const braceMatch = clause.match(/\{([\s\S]*?)\}/);
      if (braceMatch) {
        for (const part of braceMatch[1].split(",")) {
          const piece = part.trim();
          if (!piece) continue;
          const m2 = piece.match(/^([a-zA-Z_$][\w$]*)\s+as\s+([a-zA-Z_$][\w$]*)$/);
          if (m2) renameAliases.push({ from: m2[1], to: m2[2] });
        }
      }
    }
  }
  if (nsImports.length) namespaceImports.set(path, nsImports);
  if (renameAliases.length) renameAliasesPerFile.set(path, renameAliases);
  visited.set(path, { src, deps });
  moduleExports.set(path, collectExports(src));
  for (const dep of deps) readAndProcess(dep);
  order.push(path);
}

readAndProcess(ENTRY);

// Build the bundled ESM output: strip relative imports/exports, keep
// non-relative ones (none in this codebase), concatenate in topo order.
const banner =
  `/* @acs/runtime — bundled ESM build.\n` +
  ` * Generated by tools/bundle.mjs at ${new Date().toISOString()}\n` +
  ` * Source modules: ${order.length}\n` +
  ` */\n\n`;

const stripped = order.map((p) => {
  let code = visited.get(p).src;
  // Remove relative imports — they're all bundled into this file.
  code = code.replace(/import\s+(?:[\s\S]+?\s+from\s+)?["']\.[^"']+["'];?\n?/g, "");
  // `export ` keywords are kept on the entry; remove on inner files
  // (we re-export from the entry's symbol scope).
  if (p !== ENTRY) {
    code = code.replace(/^export\s+(function|const|let|var|class)/gm, "$1");
    code = code.replace(/^export\s*\{[^}]+\};?/gm, "");
  }
  // Synthesize `const ns = { name1, name2, ... };` shims for any
  // `import * as ns from "./foo.js"` we found. Without this, ns is
  // undefined where referenced after import-stripping.
  const nsForFile = namespaceImports.get(p);
  if (nsForFile) {
    const shims = nsForFile.map(({ ns, depPath }) => {
      const exports = moduleExports.get(depPath) || [];
      return `const ${ns} = { ${exports.join(", ")} };`;
    }).join("\n");
    code = shims + "\n" + code;
  }
  const renames = renameAliasesPerFile.get(p);
  if (renames) {
    const aliasLines = renames.map(({ from, to }) => `const ${to} = ${from};`).join("\n");
    code = aliasLines + "\n" + code;
  }
  return `// === ${p.replace(ROOT + "/", "")} ===\n` + code;
}).join("\n\n");

if (!existsSync(OUT_DIR)) mkdirSync(OUT_DIR, { recursive: true });

let esm = banner + stripped;

// Inline defaults.acs and the worklet source into the bundled runtime.
// This eliminates the `new URL("...", import.meta.url)` expressions that
// trip webpack/vite asset resolution at consumer build time, and removes
// the runtime fetch for defaults.acs (which fails on CDNs that don't
// serve `.acs` with CORS headers — e.g. unpkg returns no
// Access-Control-Allow-Origin for unknown extensions).
//
// We textually rewrite the resolver function bodies that the source code
// uses as bundler hooks (`__acsDefaultsSource`, `__acsDefaultsUrl`,
// `__acsWorkletUrl`). The dev path (poc served directly) keeps the
// URL-fetch behavior; only the published bundle gets the inline form.
const defaultsAcs = readFileSync(resolve(ROOT, "poc/defaults.acs"), "utf8");
const workletSrc = readFileSync(resolve(ROOT, "poc/runtime/worklets/click-processor.js"), "utf8");
const ENC_DEFAULTS = JSON.stringify(defaultsAcs);
const ENC_WORKLET = JSON.stringify(workletSrc);

// Replace bodies of the three resolver functions by finding the function
// header then walking braces to locate the matching close. Regex-only
// approaches are fragile here — non-greedy `.*?` matches across multiple
// functions and accidentally swallows neighbors.
const replaceBody = (src, fnName, newBody) => {
  const headerRe = new RegExp(`function\\s+${fnName}\\s*\\(\\s*\\)\\s*\\{`);
  const m = headerRe.exec(src);
  if (!m) throw new Error(`bundle: could not locate ${fnName}() in source`);
  const start = m.index;
  let i = m.index + m[0].length;
  let depth = 1;
  while (i < src.length && depth > 0) {
    const c = src[i++];
    if (c === "{") depth++;
    else if (c === "}") depth--;
  }
  if (depth !== 0) throw new Error(`bundle: unbalanced braces while finding end of ${fnName}()`);
  return src.slice(0, start) + `function ${fnName}() ${newBody}` + src.slice(i);
};

esm = replaceBody(esm, "__acsDefaultsSource", `{ return ${ENC_DEFAULTS}; }`);
// `__acsDefaultsUrl` is unreachable in bundled mode (source check returns
// inline first), but webpack does static analysis on the file regardless
// of branch reachability — the `new URL(..., import.meta.url)` token
// must not appear at all in the published bundle.
esm = replaceBody(esm, "__acsDefaultsUrl", `{ return null; }`);
// Worklet: build a Blob URL from inlined source so AudioWorklet.addModule
// works without a co-located `.js` asset.
esm = replaceBody(
  esm,
  "__acsWorkletUrl",
  `{ return URL.createObjectURL(new Blob([${ENC_WORKLET}], { type: "application/javascript" })); }`,
);

// Sanity check: the bundled output must not retain the patterns that
// caused the original webpack failure. Fail the build if it does.
if (esm.includes(`new URL("../defaults.acs"`) ||
    esm.includes(`new URL("./worklets/click-processor.js"`)) {
  throw new Error("bundle: post-rewrite still contains forbidden new URL(..., import.meta.url) expressions");
}

writeFileSync(resolve(OUT_DIR, "runtime.mjs"), esm);

// CJS shim: just a require() wrapper that loads the ESM via dynamic import.
const cjs =
  `/* @acs/runtime — CJS entry. Loads the ESM bundle. */\n` +
  `module.exports = (async () => (await import("./runtime.mjs")))();\n`;
writeFileSync(resolve(OUT_DIR, "runtime.cjs"), cjs);

// Also emit a parse-only entry for build-tools that just need the parser.
const parseSrc = readFileSync(resolve(ROOT, "poc/runtime/parse.js"), "utf8");
writeFileSync(resolve(OUT_DIR, "parse.mjs"), banner + parseSrc);

// `defaults.acs` and the worklet source are inlined into runtime.mjs above;
// the runtime no longer fetches either at load time. We still mirror them
// into `dist/` for direct-asset use cases (manual `<link rel="audiostyle">`
// pointing at the package's defaults, or unpkg consumers wanting to inspect
// the source).
copyFileSync(resolve(ROOT, "poc/defaults.acs"), resolve(OUT_DIR, "defaults.acs"));
const WORKLET_OUT = resolve(OUT_DIR, "worklets");
if (!existsSync(WORKLET_OUT)) mkdirSync(WORKLET_OUT, { recursive: true });
copyFileSync(
  resolve(ROOT, "poc/runtime/worklets/click-processor.js"),
  resolve(WORKLET_OUT, "click-processor.js"),
);

console.log(
  `[bundle] wrote dist/runtime.mjs (${(esm.length / 1024).toFixed(1)} KB), ` +
  `dist/runtime.cjs, dist/parse.mjs, dist/defaults.acs, dist/worklets/click-processor.js ` +
  `from ${order.length} source modules.`
);
